let isSunny = true;
let isWeekend = false;

console.log(`isSunny && isWeekend: ${isSunny && isWeekend}`);
console.log(`isSunny || isWeekend: ${isSunny || isWeekend}`);
console.log(`!isSunny: ${!isSunny}`);
console.log(`!isWeekend: ${!isWeekend}`);
console.log(`isSunny && !isWeekend: ${isSunny && !isWeekend}`);
console.log(`!isSunny || isWeekend: ${!isSunny || isWeekend}`);